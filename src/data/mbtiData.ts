export function getCompatibility(
  type1: string,
  type2: string,
): CompatibilityRating {
  return (
    compatibilityMatrix[type1]?.[type2] || {
      rating: 3,
      description: 'Compatibility data not available',
    }
  );
}

export function getMBTIType(code: string): MBTIType | undefined {
  return mbtiTypes.find((type) => type.code === code);
}
